class ArtifactManifestEntry(BaseModel):
    """Metadata for a file produced by a task — path + size, no content."""
    path: str
    size_bytes: int
    file_type: str = ""  # extension: "py", "md", "json", etc.


class TaskResult(BaseModel):
    """Output produced by an agent for a single task."""
    output: str = ""
    artifacts: dict[str, str] = Field(
        default_factory=dict,
        description="name → content mapping of files/artifacts produced",
    )
    file_manifest: list[ArtifactManifestEntry] = Field(
        default_factory=list,
        description="Authoritative record of files produced (path + size, no content)",
    )
    generated_tests: list[GeneratedTest] = Field(default_factory=list)
    error: str | None = None
    token_usage: int = 0
    token_usage_detailed: TokenUsage | None = None
    wall_seconds: float = 0.0
    learnings: list[str] = Field(default_factory=list)


class GeneratedTest(BaseModel):
    """A test generated by an agent to verify its own output."""
    filename: str
    content: str
    test_type: str = "unit"
    framework: str = "pytest"


class StructuredTaskPrompt(BaseModel):
    """Structured format for task descriptions, validated by the plan checker."""
    objective: str = Field(default="", description="What must be accomplished")
    files: list[str] = Field(default_factory=list, description="File scope — only touch these")
    action: str = Field(default="", description="Specific steps to take")
    verify: str = Field(default="", description="How to verify the output is correct")
    done: str = Field(default="", description="Definition of done")


class Task(BaseModel):
    """Smallest unit of work — assigned to a single agent."""
    id: str
    workstream_id: str
    workflow_id: str

    title: str
    description: str
    structured_prompt: StructuredTaskPrompt | None = None
    status: TaskStatus = TaskStatus.PENDING

    assigned_agent_id: str | None = None
    depends_on: list[str] = Field(
        default_factory=list,
        description="Task IDs that must complete before this task starts",
    )
    acceptance_criteria_ids: list[str] = Field(
        default_factory=list,
        description="Which spec-level acceptance criteria this task contributes to",
    )

    # Verification
    verification_strategy: str = Field(
        default="llm_judge",
        description="'llm_judge', 'test', 'manual', or custom verifier name",
    )
    skeleton_tests: list[str] = Field(
        default_factory=list,
        description="Skeleton test code the agent must implement and pass",
    )

    # Execution tracking
    result: TaskResult | None = None
    attempt: int = 0
    max_retries: int = 3
    created_at: datetime = Field(default_factory=datetime.utcnow)
    started_at: datetime | None = None
    completed_at: datetime | None = None

    # Verification feedback for retry context (WS-1.2)
    attempt_feedback: list[VerificationFeedback] = Field(default_factory=list)

    # Linear sync (WS-1.3)
    linear_issue_id: str | None = None

    # Transient — set by orchestrator before dispatch, not persisted
    enriched_prompt: str | None = Field(
        default=None,
        exclude=True,
        description="Pre-built context prompt from ContextBuilder. Agents use this when set.",
    )

    def content_hash(self) -> str:
        """Hash for dedup — two tasks with the same hash are duplicates."""
        payload = json.dumps(
            {"title": self.title, "description": self.description, "workstream_id": self.workstream_id},
            sort_keys=True,
        )
        return hashlib.sha256(payload.encode()).hexdigest()[:16]

    @property
    def is_terminal(self) -> bool:
        return self.status in (
            TaskStatus.PASSED,
            TaskStatus.FAILED,
            TaskStatus.SKIPPED,
            TaskStatus.CANCELLED,
        )


