class TestCaseResult(BaseModel):
    """Result of a single test case from structured test parsing."""
    __test__ = False  # Prevent pytest collection
    name: str
    passed: bool
    error_message: str = ""


class VerificationFeedback(BaseModel):
    """Feedback from a single verification attempt, stored for retry context."""
    attempt: int
    verifier_type: str  # "llm_judge", "test_runner", "chained"
    passed: bool
    score: float = 0.0
    feedback: str = ""
    suggested_improvements: list[str] = Field(default_factory=list)
    test_results: list[TestCaseResult] = Field(default_factory=list)


