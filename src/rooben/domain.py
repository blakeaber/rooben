"""
Core domain models — the runtime state of orchestration.

These are distinct from the *spec* models: the spec is the input contract,
these models track execution state as agents work.
"""

from __future__ import annotations

import hashlib
import json
from datetime import datetime
from enum import Enum

from pydantic import BaseModel, Field, computed_field


# ---------------------------------------------------------------------------
# Enums
# ---------------------------------------------------------------------------

class TaskStatus(str, Enum):
    PENDING = "pending"
    BLOCKED = "blocked"      # waiting on dependencies
    READY = "ready"          # all deps satisfied, waiting for agent
    IN_PROGRESS = "in_progress"
    VERIFYING = "verifying"  # task output is being verified
    PASSED = "passed"        # verification succeeded
    FAILED = "failed"        # verification failed (may retry)
    SKIPPED = "skipped"
    CANCELLED = "cancelled"


class WorkstreamStatus(str, Enum):
    PENDING = "pending"
    IN_PROGRESS = "in_progress"
    COMPLETED = "completed"
    FAILED = "failed"
    CANCELLED = "cancelled"


class WorkflowStatus(str, Enum):
    PENDING = "pending"
    PLANNING = "planning"
    IN_PROGRESS = "in_progress"
    COMPLETED = "completed"
    FAILED = "failed"
    CANCELLED = "cancelled"


# ---------------------------------------------------------------------------
# Token tracking (WS-1.1)
# ---------------------------------------------------------------------------

class TokenUsage(BaseModel):
    """Detailed token usage from a single LLM generation."""
    input_tokens: int = 0
    output_tokens: int = 0
    cache_read_tokens: int = 0
    cache_creation_tokens: int = 0

    @computed_field  # type: ignore[prop-decorator]
    @property
    def total(self) -> int:
        return self.input_tokens + self.output_tokens

    def __add__(self, other: "TokenUsage") -> "TokenUsage":
        return TokenUsage(
            input_tokens=self.input_tokens + other.input_tokens,
            output_tokens=self.output_tokens + other.output_tokens,
            cache_read_tokens=self.cache_read_tokens + other.cache_read_tokens,
            cache_creation_tokens=self.cache_creation_tokens + other.cache_creation_tokens,
        )


